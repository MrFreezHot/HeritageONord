										<h2 class="font-weight-semi-bold"><a href="#">Celebration des 220 ans du bicolore</a></h2>

										<div class="post-meta">
											<span><i class="far fa-user"></i> Par <a href="#">Binedinie Jean-Louis</a> </span>
											<span><i class="far fa-folder"></i> <a href="#">Innovation</a>, <a href="#">Education</a> </span>
											<span><i class="far fa-comments"></i> <a href="#">12 Commentaires</a></span>
										</div>

										<p>L'étendard haïtien a pour emblème un palmier couronné d'un bonnet phrygien aux couleurs du drapeau national, qui sont le bleu et le rouge, d'où on l'appelle le bicolore. Il a vu le jour le 18 mai 1803 dans le département de l'Ouest, plus précisément dans la commune de l'Arcahaie. Depuis lors, cette date est consacrée à la célébration du bicolore. Généralement, l'Arcahaie accueillait la célébration officielle de notre fierté haïtienne avec toute son ampleur et sa fierté historique pour cette commune. Cette année, c'était différent.</p>
										<img src="img/article/18mai21.jpg" class="img-fluid float-start me-4 mt-2" alt="" />
										<p>Depuis quelques années, l'insécurité bat son plein au niveau du département de l'Ouest, paralysant presque toutes les communes de ce même département, et l'accès à d'autres départements devient inaccessible. De ce fait, l'Arcahaie n'a pas pu accueillir la célébration officielle des 220 ans de notre bicolore. Le Premier ministre haïtien, lors de son message adressé à la nation à l'occasion de la fête de l'agriculture et du travail le premier mai, avait annoncé que la célébration officielle du bicolore haïtien se ferait au Cap-Haïtien.</p>
										<p>Le chef-lieu du département du Nord, la deuxième ville du pays, connue pour sa notoriété et sa gloire dans notre histoire, devient la ville la plus fréquentée en raison de la paix, de la sécurité et de son hospitalité. Le pouvoir de facto en avait profité pour la célébration officielle du 220e anniversaire de notre bicolore, avec la collaboration des autorités locales. Généralement, le Cap-Haïtien, baptisé "Cité du Roi Henry Christophe", fêtait le bicolore de manière grandiose, mais cette année, c'était encore plus extraordinaire. Dans l'après-midi, sur le boulevard de la Cité du Roi Baptiseur, en présence du Premier ministre Dr Ariel Henry et d'autres membres du pouvoir central tels que le ministère de l'Éducation nationale et de la Formation professionnelle, notamment Monsieur Nesmy Manigat, le ministre de l'Intérieur, le directeur général de la police et les autorités de la cité christophienne, la parade se déroulait. Plusieurs institutions telles que la Force Armée d'Haïti (FADH), la police et des écoles de cette ville comme le Collège Pratique du Nord (CPN), le Collège Christ Roi (CCR), l'Institution Royale du Cap-Haïtien (IRCAPH), le Collège Notre Dame du Cap-Haïtien accompagnés des élèves des sœurs de Saint Joseph de Cluny et des sœurs de Regina, l'École de Musique du Cap-Haïtien (Cemuchca) et la fanfare de Louis Mercier avaient participé à cette revue. Depuis quelques années, aucune activité culturelle ne se déroule sans la présence de l'Institut Sacré-Cœur du Cap-Haïtien (ISCCH), connu pour ses couleurs, sa logistique, son amour, sa passion et sa contribution aux festivités de la ville. On pouvait remarquer la présence des élèves du jardin d'enfants, du primaire, du secondaire et de l'école professionnelle. Ils étaient tous au rendez-vous.</p>
										<p>Les 220 ans de notre étendard avaient été chaleureusement célébrés au Cap-Haïtien, et de nombreux touristes locaux et internationaux avaient fait le déplacement pour cette grande célébration qui marque notre fierté.</p>

